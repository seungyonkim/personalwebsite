package com.mygdx.game;

public class Person{
    private String type;
    private String name;
    private String message;

    public void setType(String n){
        type = n;
    }

    public String getType(){
        return type;
    }

    public void setName(String n){
        name = n;
    }

    public String getName(){
        return name;
    }

    public void setMessage(String n){
        message = n;
    }

    public String getMessage(){
        return message;
    }
}
